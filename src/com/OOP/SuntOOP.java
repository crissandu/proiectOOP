package com.OOP;

public class SuntOOP {

    public int numarul1 = 20;
    private int numarul2 = 30;
    protected int numarul3 = 40;

    public static void main(String[] args) {
        SuntOOP o = new SuntOOP();
        System.out.println(o.numarul1);
        System.out.println(o.numarul2);
        System.out.println(o.numarul3);
    }

}
